void genOh1(std::vector<double> & Weights, matrixE & Grid, double value, int shift) {
  //function that generates 6 new points with Oh symmetry from [0,0,1]
  double aconst = 1.0;
  Grid(1,1 + shift) = aconst;
  Grid(1,2 + shift) = -aconst;
  Grid(2,3 + shift) = aconst;
  Grid(2,4 + shift) = -aconst;
  Grid(3,5 + shift) = aconst;
  Grid(3,6 + shift) = -aconst;
  for (size_t idx = 0; idx < 6; ++idx) {
    Weights[idx + shift] = value;
  }
}
